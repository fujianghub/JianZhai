/**
 * 行内数学 `$…$` 的输入规则 / 粘贴规则共享正则（纯模块，无依赖，可单测）。
 *
 * 边界约束与阅读端 `markdown.ts katexPlugin` 的行内规则对齐：
 * - 开 `$` 前不能是数字（货币 `5$`）、反斜杠（转义 `\$`）或另一个 `$`（块级）；
 * - 内容首尾不能是空白（`$ x $` 不识别，`5 to 10` 之类的货币区间不受影响）；
 * - 内容不含 `$` 与换行。
 *
 * 输入规则在打出闭合 `$` 的瞬间触发（锚定 `$` 结尾），此时闭合符后面还没有
 * 字符，故「闭 `$` 后非数字」守卫只存在于粘贴规则里。
 */

/** MathInline 输入规则：行内打完 `$x$` 即转公式节点。单字符内容（`$x$`）也支持。 */
export const INLINE_MATH_INPUT_RE = /(?<![\d\\$])\$(?!\$)([^\s$](?:[^$\n]*[^\s$])?)\$$/;

/** MathInline 粘贴规则：粘贴纯文本里的 `$…$` 批量转公式节点。 */
export const INLINE_MATH_PASTE_RE = /(?<![\d\\$])\$(?!\$)([^\s$][^$\n]*?[^\s$])\$(?![\d$])/g;
